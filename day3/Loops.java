public class Loops{
    public static void main(String args[]){
        //while loop
        int i = 1;
        while(i<=10){
            System.out.println(i);
            i++;
        }
    
        //dowhile loop 
        int j = 11; 
        do{
            System.out.println(j);
            j++;
        } while(j<=20);
        
        //for loop
         for(int k=21; k<30; k++){
            System.out.println(k);
         }


        
    }
}