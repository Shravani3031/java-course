public class SwitchCase {
    public static void main(String args[]) {
        switch (2) {
            case 1:
                System.out.println("case 1 satisfied");
                break;
            case 2:
                System.out.println("case 2 satisfied");
                break;
            case 3:
                System.out.println("case 3 satisfied");
                break;
            case 4:
                System.out.println("case 4 satisfied");
                break;
            case 5:
                System.out.println("case 5 satisfied");
                break;
            default:
                System.out.println("no case satisfied");
                break;
        }
    }
}